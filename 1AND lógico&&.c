// Primeiro bloco de código
#include <stdio.h>  // Biblioteca padrão para entrada e saída de dados

int main() {
  int a = 5;   // Declara a variável 'a' e atribui o valor 5
  int b = 10;  // Declara a variável 'b' e atribui o valor 10

  // Verifica se 'a' e 'b' são maiores que zero ao mesmo tempo (ambos positivos)
  if (a > 0 && b > 0) {
    // Se a condição for verdadeira, exibe a mensagem
    printf("Ambos os números são positivos\n");
  }

  return 0;  // Finaliza o programa
}
