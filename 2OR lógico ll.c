// Segundo bloco de código
#include <stdio.h>  // Biblioteca padrão

int main() {
  int a = 5;     // Variável 'a' recebe valor 5
  int b = -10;   // Variável 'b' recebe valor negativo (-10)

  // Verifica se pelo menos um dos dois números é maior que zero (positivo)
  if (a > 0 || b > 0) {
    // Se a condição for verdadeira, exibe a mensagem
    printf("Pelo menos um dos números é positivo\n");
  }

  return 0;  // Fim do programa
}
